//IMAGEM 1
//get modal
var modal = document.getElementById("meuModal1");

//pegar imagem e inserir modal - alt é legenda
var img = document.getElementById("imgEntremet");
var modalImg = document.getElementById("img01");
var captionText = document.getElementById("caption");
img.onclick = function(){
    modal.style.display = "block";
    modalImg.src = this.src;
    captionText:this.innerHTML = this.alt;
}

//span fechar
var span = document.getElementsByClassName("fechar1")[0];

//fehcar ao clicar no span fechar
span.onclick = function() {
    modal.style.display = "none";
}


